// BONUS: Make a program that can subtract, multiply, and also divide!

// Addition
var firstNumber = prompt("What is your First Number?");
var secondNumber = prompt("What is your Second Number?");
var sum = Number(firstNumber) + Number(secondNumber);
alert(firstNumber + "plus" + secondNumber + "=" + sum);

// Substraction
var firstNumber = prompt("What is your First Number?");
var secondNumber = prompt("What is your Second Number?");
var sum = Number(firstNumber) - Number(secondNumber);
alert(firstNumber + "minus" + secondNumber + "=" + sum);

// Multiply
var firstNumber = prompt("What is your First Number?");
var secondNumber = prompt("What is your Second Number?");
var sum = Number(firstNumber) * Number(secondNumber);
alert(firstNumber + "times" + secondNumber + "=" + sum);

// Divide
var firstNumber = prompt("What is your First Number?");
var secondNumber = prompt("What is your Second Number?");
var sum = Number(firstNumber) / Number(secondNumber);
alert(firstNumber + "divided by" + secondNumber + "=" + sum);


